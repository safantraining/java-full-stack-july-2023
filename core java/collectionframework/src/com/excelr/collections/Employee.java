package com.excelr.collections;

public class Employee {

	private int id;
	private String name;
	private String dept;
	private int salary;
	private int age;
	
	
	public Employee() {
		super();
	}


	public Employee(int id, String name, String dept, int salary, int age) {
		super();
		this.id = id;
		this.name = name;
		this.dept = dept;
		this.salary = salary;
		this.age = age;
	}
	
	
}
